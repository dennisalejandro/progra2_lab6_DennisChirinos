    private void JTR_TreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTR_TreeMouseClicked
        if (evt.isMetaDown()) {
            int y = evt.getY();
            int x = evt.getX();
            int row = JTR_Tree.getClosestRowForLocation(evt.getX(), evt.getY());
            Object v1 = JTR_Tree.getSelectionPath().getLastPathComponent();
            Nodo_Seleccionado = (DefaultMutableTreeNode) v1;
            if ((Nodo_Seleccionado.getUserObject() instanceof Lugar)) {
                PopUp_Remove.show(JTR_Tree, x, y);
            }
        }
    }//GEN-LAST:event_JTR_TreeMouseClicked

