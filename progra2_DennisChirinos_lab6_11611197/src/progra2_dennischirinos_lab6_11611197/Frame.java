    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String Nombre = TF_Nombre.getText();
        String Direccion = TA_Direccion.getText();
        int Valor = Integer.parseInt(TF_Valor.getText());
        String Categoria_Res = CB_Categoria_Res.getSelectedItem().toString();
        String Categoria_Can = CB_Categoria_Can.getSelectedItem().toString();
        String Estado = TF_Estado.getText();
        int Calificacion = (Integer) SP_Calificacion.getValue();
        if (RB_Res.isSelected()) {
            if (Valor>5) {
            Valor = 0;
            }
            Restaurante c = new Restaurante(Categoria_Res, Valor, Nombre, Direccion, Calificacion);
            ListaRestaurantes.add(c);
        }
        if (RB_Can.isSelected()) {
            Cancha c = new Cancha(Categoria_Can, Estado, Nombre, Direccion, Calificacion);
            ListaCanchas.add(c);
        }
        if (RB_Casa.isSelected()) {
            Casas c = new Casas(Nombre, Direccion, Valor);
            ListaCasas.add(c);
        }
        TF_Valor.setText("");
        TF_Nombre.setText("");
        TA_Direccion.setText("");
        actualizar();
    }//GEN-LAST:event_jButton3ActionPerformed

