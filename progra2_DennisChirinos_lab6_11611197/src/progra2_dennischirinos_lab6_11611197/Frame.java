    private void RBT_CarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RBT_CarActionPerformed
        RBT_Can.setSelected(false);
        RBT_Res.setSelected(false);
        RBT_Casa.setSelected(false);
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Numero");
        model.addColumn("Distancia");
        model.addColumn("Inicio");
        model.addColumn("Final");
        if (ListaCarreteras.size() >= 0) {
            Object[] o = new Object[model.getColumnCount()];
            for (int i = 0; i < ListaCarreteras.size(); i++) {
                o[0] = ListaCarreteras.get(i).getNumero();
                o[1] = ListaCarreteras.get(i).getDistancia();
                o[2] = ListaCarreteras.get(i).getInicio();
                o[3] = ListaCarreteras.get(i).getFinal();
                model.addRow(o);
            }
        }
        JT_Tabla.setModel(model);

    }//GEN-LAST:event_RBT_CarActionPerformed

