    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int No = Integer.parseInt(TF_No.getText());
        double Dis = Double.parseDouble(TF_Distancia.getText());
        int i3 = CB_Entrada.getSelectedIndex();
        int i2 = CB_Salida.getSelectedIndex();
        Lugar Inicio = ListaLugares.get(i3);
        Lugar Final = ListaLugares.get(i2);
        Carretera c = new Carretera(No, Dis, Inicio, Final);
        for (int i = 0; i < ListaRestaurantes.size(); i++) {
            for (int j = 0; j < ListaLugares.size(); j++) {
                if (ListaLugares.get(j).Nombre.equals(ListaRestaurantes.get(i).getNombre())) {
                    System.out.println("Paso!");
                    ListaRestaurantes.get(i).setEntrada(c);
                    ListaRestaurantes.get(i).setSalida(c);
                }
            }
        }
        for (int i = 0; i < ListaCanchas.size(); i++) {
            for (int j = 0; j < ListaLugares.size(); j++) {
                if (ListaLugares.get(j).Nombre.equals(ListaCanchas.get(i).getNombre())) {
                    System.out.println("Paso!");
                    ListaCanchas.get(i).setEntrada(c);
                    ListaCanchas.get(i).setSalida(c);
                }
            }
        }
        for (int i = 0; i < ListaCasas.size(); i++) {
            for (int j = 0; j < ListaLugares.size(); j++) {
                if (ListaLugares.get(j).Nombre.equals(ListaCasas.get(i).getNombre())) {
                    System.out.println("Paso!");
                    ListaCasas.get(i).setEntrada(c);
                    ListaCasas.get(i).setSalida(c);
                }
            }
        }
        ListaLugares.get(i3).setEntrada(c);
        ListaLugares.get(i2).setSalida(c);
        ListaCarreteras.add(c);
        NumCarretera++;
        TF_No.setText("" + NumCarretera);
        actualizar();
    }//GEN-LAST:event_jButton2ActionPerformed

