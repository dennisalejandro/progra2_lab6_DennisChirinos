    public void actualizar() {
        DefaultMutableTreeNode Raiz = new DefaultMutableTreeNode("Locaciones");
        DefaultMutableTreeNode Lugares = new DefaultMutableTreeNode("Lugares");
        DefaultMutableTreeNode ND_Restaurantes = new DefaultMutableTreeNode("Restaurantes");
        DefaultMutableTreeNode ND_Canchas = new DefaultMutableTreeNode("Canchas");
        DefaultMutableTreeNode ND_Casas = new DefaultMutableTreeNode("Casas");
        DefaultMutableTreeNode ND_Carreteras = new DefaultMutableTreeNode("Carreteras");
        Raiz.add(Lugares);
        Lugares.add(ND_Restaurantes);
        Lugares.add(ND_Canchas);
        Lugares.add(ND_Casas);
        Raiz.add(ND_Carreteras);
        DefaultTreeModel model = new DefaultTreeModel(Raiz);
        for (int i = 0; i < ListaRestaurantes.size(); i++) {
            Restaurante Res = ListaRestaurantes.get(i);
            DefaultMutableTreeNode Node_Restaurante = new DefaultMutableTreeNode(Res);
            DefaultMutableTreeNode Node_Restaurante_Nombre = new DefaultMutableTreeNode(Res.getNombre());
            DefaultMutableTreeNode Node_Restaurante_Valor = new DefaultMutableTreeNode(Res.getValor());
            DefaultMutableTreeNode Node_Restaurante_Categoria = new DefaultMutableTreeNode(Res.getCategoria());
            DefaultMutableTreeNode Node_Restaurante_Calificacion = new DefaultMutableTreeNode(Res.getCalificacion());
            Node_Restaurante.add(Node_Restaurante_Nombre);
            Node_Restaurante.add(Node_Restaurante_Valor);
            Node_Restaurante.add(Node_Restaurante_Categoria);
            Node_Restaurante.add(Node_Restaurante_Calificacion);
            ND_Restaurantes.add(Node_Restaurante);
        }
        for (int i = 0; i < ListaCanchas.size(); i++) {
            Cancha Can = ListaCanchas.get(i);
            DefaultMutableTreeNode Node_Cancha = new DefaultMutableTreeNode(Can);
            DefaultMutableTreeNode Node_Cancha_Direccion = new DefaultMutableTreeNode(Can.getDireccion());
            DefaultMutableTreeNode Node_Cancha_Valor = new DefaultMutableTreeNode(Can.getValor());
            DefaultMutableTreeNode Node_Cancha_Estado = new DefaultMutableTreeNode(Can.getEstado());
            DefaultMutableTreeNode Node_Cancha_Categoria = new DefaultMutableTreeNode(Can.getCategoria());
            Node_Cancha.add(Node_Cancha_Direccion);
            Node_Cancha.add(Node_Cancha_Valor);
            Node_Cancha.add(Node_Cancha_Estado);
            Node_Cancha.add(Node_Cancha_Categoria);
            ND_Canchas.add(Node_Cancha);
        }
        for (int i = 0; i < ListaCasas.size(); i++) {
            Casas Can = ListaCasas.get(i);
            DefaultMutableTreeNode Node_Casa = new DefaultMutableTreeNode(Can);
            DefaultMutableTreeNode Node_Casa_Direccion = new DefaultMutableTreeNode(Can.getDireccion());
            DefaultMutableTreeNode Node_Casa_Valor = new DefaultMutableTreeNode(Can.getValor());
            Node_Casa.add(Node_Casa_Direccion);
            Node_Casa.add(Node_Casa_Valor);
            ND_Casas.add(Node_Casa);
        }
        for (int i = 0; i < ListaCarreteras.size(); i++) {
            Carretera Can = ListaCarreteras.get(i);
            DefaultMutableTreeNode Node_Carretera = new DefaultMutableTreeNode(Can);
            DefaultMutableTreeNode Node_Car_Num = new DefaultMutableTreeNode(Can.getNumero());
            DefaultMutableTreeNode Node_Car_Dis = new DefaultMutableTreeNode(Can.getDistancia());
            DefaultMutableTreeNode Node_Car_Ini = new DefaultMutableTreeNode(Can.getInicio());
            DefaultMutableTreeNode Node_Car_Fin = new DefaultMutableTreeNode(Can.getFinal());
            Node_Carretera.add(Node_Car_Num);
            Node_Carretera.add(Node_Car_Dis);
            Node_Carretera.add(Node_Car_Ini);
            Node_Carretera.add(Node_Car_Fin);
            ND_Carreteras.add(Node_Carretera);
        }
        JTR_Tree.setModel(null);
        JTR_Tree.setModel(model);
    }

